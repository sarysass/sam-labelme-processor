"""
Tests for result writer module.
"""

import json
from pathlib import Path

import sys

sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

from src.core.data_manager import DataItem
from src.core.labelme_io import BBoxShape, ImageInfo, MaskShape
from src.core.result_writer import ResultWriter


class TestResultWriter:
    """Tests for writing configured processing outputs."""

    def test_write_separate_output_uses_relative_image_path(self, tmp_path):
        """Test: Separate output stores imagePath relative to the JSON file."""
        image_path = tmp_path / "images" / "nested" / "sample.png"
        image_path.parent.mkdir(parents=True)
        image_path.write_bytes(b"")

        mask_json_path = tmp_path / "mask" / "nested" / "sample.json"
        data_item = DataItem(
            image_path=image_path,
            bbox_path=tmp_path / "bbox" / "nested" / "sample.json",
            mask_path=mask_json_path,
            relative_path=Path("nested/sample.png"),
            image_id="sample",
        )
        image_info = ImageInfo(image_path="sample.png", image_height=12, image_width=16)
        mask_shapes = [
            MaskShape(
                label="mask",
                points=[[1.0, 1.0], [4.0, 4.0]],
                group_id=0,
                description="Generated by SAM",
                mask="dGVzdA==",
            )
        ]

        writer = ResultWriter(output_separate=True, output_combine=False)
        output_paths = writer.write_outputs(
            data_item=data_item,
            image_path=image_path,
            image_info=image_info,
            bbox_shapes=[],
            mask_shapes=mask_shapes,
        )

        assert output_paths.mask_json_path == mask_json_path
        data = json.loads(mask_json_path.read_text())
        assert data["imagePath"] == "../../images/nested/sample.png"
        assert data["shapes"][0]["shape_type"] == "mask"

    def test_write_combined_output_preserves_bbox_and_mask_entries(self, tmp_path):
        """Test: Combined output contains both rectangle and mask shapes."""
        image_path = tmp_path / "images" / "sample.png"
        image_path.parent.mkdir(parents=True)
        image_path.write_bytes(b"")

        data_item = DataItem(
            image_path=image_path,
            bbox_path=tmp_path / "bbox" / "sample.json",
            mask_path=tmp_path / "mask" / "sample.json",
            relative_path=Path("sample.png"),
            image_id="sample",
        )
        image_info = ImageInfo(image_path="sample.png", image_height=10, image_width=10)
        bbox_shapes = [
            BBoxShape(label="worm", points=[[1.0, 1.0], [5.0, 5.0]], group_id=1)
        ]
        mask_shapes = [
            MaskShape(
                label="mask",
                points=[[1.0, 1.0], [5.0, 5.0]],
                group_id=1,
                description="Generated by SAM",
                mask="dGVzdA==",
            )
        ]

        writer = ResultWriter(
            output_separate=False,
            output_combine=True,
            combined_dir=tmp_path / "combined",
        )
        output_paths = writer.write_outputs(
            data_item=data_item,
            image_path=image_path,
            image_info=image_info,
            bbox_shapes=bbox_shapes,
            mask_shapes=mask_shapes,
        )

        assert output_paths.combined_json_path == tmp_path / "combined" / "sample.json"
        data = json.loads(output_paths.combined_json_path.read_text())
        assert len(data["shapes"]) == 2
        assert [shape["shape_type"] for shape in data["shapes"]] == ["rectangle", "mask"]

    def test_write_outputs_supports_both_modes(self, tmp_path):
        """Test: Writing both separate and combined outputs returns both paths."""
        image_path = tmp_path / "images" / "sample.png"
        image_path.parent.mkdir(parents=True)
        image_path.write_bytes(b"")

        data_item = DataItem(
            image_path=image_path,
            bbox_path=tmp_path / "bbox" / "sample.json",
            mask_path=tmp_path / "mask" / "sample.json",
            relative_path=Path("sample.png"),
            image_id="sample",
        )
        image_info = ImageInfo(image_path="sample.png", image_height=8, image_width=8)
        mask_shapes = [
            MaskShape(
                label="mask",
                points=[[0.0, 0.0], [3.0, 3.0]],
                group_id=0,
                description="Generated by SAM",
                mask="dGVzdA==",
            )
        ]

        writer = ResultWriter(
            output_separate=True,
            output_combine=True,
            combined_dir=tmp_path / "combined",
        )
        output_paths = writer.write_outputs(
            data_item=data_item,
            image_path=image_path,
            image_info=image_info,
            bbox_shapes=[],
            mask_shapes=mask_shapes,
        )

        assert output_paths.mask_json_path == tmp_path / "mask" / "sample.json"
        assert output_paths.combined_json_path == tmp_path / "combined" / "sample.json"
